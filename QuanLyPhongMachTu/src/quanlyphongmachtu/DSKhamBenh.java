    public void showData(){
        //tao ten cot
        Vector cols = new Vector();
        cols.addElement("Họ tên");
        cols.addElement("Giới tính");
        cols.addElement("Năm sinh");
        cols.addElement("Địa chỉ");
        //tao vector du lieu
        Vector data = new Vector();
        Date today = new Date(System.currentTimeMillis());
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String temp = format.format(today);
        BenhNhanDAO bn = new BenhNhanDAO();
        List<BenhNhan> ds = bn.laydanhsach();
        for (int i = 0; i < ds.size(); i++){
            BenhNhan a = ds.get(i);
            if (a.getNgaykham().toString().equals(temp)){
                Vector benhnhan = new Vector();
                benhnhan.addElement(a.getHoten());
                benhnhan.addElement(a.getGioitinh());
                benhnhan.addElement(a.getNamsinh());
                benhnhan.addElement(a.getDiachi());
                data.add(benhnhan);
            }
        }
        jTableDSKhamBenh.setModel(new DefaultTableModel(data, cols));
    }
