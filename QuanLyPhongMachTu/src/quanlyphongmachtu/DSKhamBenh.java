    private void jTableDSKhamBenhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDSKhamBenhMouseClicked
        // TODO add your handling code here:
        int row = jTableDSKhamBenh.getSelectedRow();
        if (row != -1){
            txtHoten.setText(jTableDSKhamBenh.getValueAt(row, 0).toString());
            cboGioitinh.setSelectedItem(jTableDSKhamBenh.getValueAt(row, 1).toString());
            txtNamsinh.setText(jTableDSKhamBenh.getValueAt(row, 2).toString());
            txtDiachi.setText(jTableDSKhamBenh.getValueAt(row, 3).toString());
        }
    }//GEN-LAST:event_jTableDSKhamBenhMouseClicked

